# WAP Data Indexing and Selection

# Data indexing and selection using a Python list
data_list = [10, 20, 30, 40, 50, 60, 70]

# Accessing elements using indexing
first_element = data_list[0]  # First element
last_element = data_list[-1]  # Last element
sublist = data_list[2:5]  # Slicing from index 2 to 4

# Printing the results
print("First element in the list:", first_element)
print("Last element in the list:", last_element)
print("Sublist from index 2 to 4:", sublist)
